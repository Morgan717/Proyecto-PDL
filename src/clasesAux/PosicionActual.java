package clasesAux;

public class PosicionActual {

        private String produccion;
        private String tokenActual;
        private String tokenSig;

    public PosicionActual() {
    }

    public String getProduccion() {
        return produccion;
    }

    public String getTokenActual() {
        return tokenActual;
    }

    public String getTokenSig() {
        return tokenSig;
    }

    public void setProduccion(String produccion) {
        this.produccion = produccion;
    }

    public void setTokenActual(String tokenActual) {
        this.tokenActual = tokenActual;
    }

    public void setTokenSig(String tokenSig) {
        this.tokenSig = tokenSig;
    }
}
